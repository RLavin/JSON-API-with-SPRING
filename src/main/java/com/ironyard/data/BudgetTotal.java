public class BudgetTotal {

    private String cat;
    private long acttotal;
    private long budtotal;

    public String getCat() {
        return cat;
    }

    public void setCat(String cat) {
        this.cat = cat;
    }

    public long getActtotal() {
        return acttotal;
    }

    public void setActtotal(long acttotal) {
        this.acttotal = acttotal;
    }

    public long getBudtotal() {
        return budtotal;
    }

    public void setBudtotal(long budtotal) {
        this.budtotal = budtotal;
    }

}
